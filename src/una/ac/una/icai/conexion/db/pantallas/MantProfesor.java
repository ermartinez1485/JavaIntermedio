    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        Profesor profe = new Profesor();
        //validar que se ingrese los datos
        profe.setCedula(new Integer(this.txtCedula.getText()));
        profe.setNombre(this.txtNombre.getText());
        profe.setSalario(new Double(this.txtSalario.getText()));
        profe.setDireccion(this.txtDireccion.getText());
        
        int i = this.profeAD.insertar(profe);
        switch(i){
        
            case 0: JOptionPane.showMessageDialog(this, "El profesor se agrego satisfactoriamente");
                break;
            case 1: JOptionPane.showMessageDialog(this, "Error, no se pudo agregar a la base de datos");
                break;
            case 2: JOptionPane.showMessageDialog(this, "Ocurrio un error");
                break;
            case 3: JOptionPane.showMessageDialog(this, "la cedula del profesor ya existe en la base de ");
        }
        this.llenarTabla();
        
    }//GEN-LAST:event_jButton1ActionPerformed

