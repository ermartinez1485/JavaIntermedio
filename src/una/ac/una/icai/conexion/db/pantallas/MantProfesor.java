    private void llenarTabla(){
    
        this.modelProfesores = new  DefaultTableModel();
        this.modelProfesores.setColumnCount(0);
        this.modelProfesores.addColumn("cedula");
        this.modelProfesores.addColumn("nombre");
        this.modelProfesores.addColumn("salario");
        this.modelProfesores.addColumn("direccion");
        
        List<Profesor> lista = this.profeAD.consultaTodos();
        this.modelProfesores.setNumRows(lista.size());
        
        for (int i = 0; i < lista.size(); i++) {
            Profesor prof = lista.get(i);
            this.modelProfesores.setValueAt(prof.getCedula(), i, 0);
            this.modelProfesores.setValueAt(prof.getNombre(), i, 1);
            this.modelProfesores.setValueAt(prof.getSalario(), i, 2);
            this.modelProfesores.setValueAt(prof.getCedula(), i, 3);
            
        }
        
        this.tablaProfesores.setModel(modelProfesores);
    }
