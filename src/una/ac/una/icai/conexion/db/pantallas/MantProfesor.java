    private void btnConsultaPorNombreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultaPorNombreActionPerformed
        // TODO add your handling code here:
        
        Profesor profe = new Profesor();
        profe.setNombre(this.txtNombre.getText());
        
        List<Profesor> lista = this.profeAD.consultaXNombre(profe.getNombre());
        this.modelProfesores.setNumRows(lista.size());
        
        for (int i = 0; i < lista.size(); i++) {
            Profesor prof = lista.get(i);
            this.modelProfesores.setValueAt(prof.getCedula(), i, 0);
            this.modelProfesores.setValueAt(prof.getNombre(), i, 1);
            this.modelProfesores.setValueAt(prof.getSalario(), i, 2);
            this.modelProfesores.setValueAt(prof.getCedula(), i, 3);
            
        }
        
        this.tablaProfesores.setModel(modelProfesores);
        
        
    }//GEN-LAST:event_btnConsultaPorNombreActionPerformed

